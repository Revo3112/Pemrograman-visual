# Membuat variabel boolean
a = True
b = False

print("Nilai a:", a)
print("Nilai b:", b)

# Operasi logika AND
print("a AND b:", a and b)

# Operasi logika OR
print("a OR b:", a or b)

# Operasi logika NOT
print("NOT a:", not a)
print("NOT b:", not b)

# Menyimpan hasil operasi boolean ke dalam variabel
c = a and b
print("Nilai c:", c)
